// Regenerates public/js/mc-preview-payload.js from the canonical Radiohead fixture.
// Run this whenever the intelligence assembler output shape changes:
//
//   node scripts/generate-preview-payload.mjs

import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath }              from 'url';
import { join, dirname }              from 'path';
import { assembleCio }                    from '../api/_lib/cio-assembler.js';
import { runIntelligenceEngine }          from '../api/_lib/intelligence-engine.js';
import { assembleIdentityIntelligence }   from '../api/_lib/identity-intelligence.js';
import { assemblePublishingIntelligence } from '../api/_lib/publishing-intelligence.js';
import { assembleBackendIntelligence }    from '../api/_lib/backend-intelligence.js';
import { assembleCatalogIntelligence }    from '../api/_lib/catalog-intelligence.js';
import { assembleCatalogEvidence }        from '../api/_lib/catalog-evidence.js';
import { assembleGlobalMusicFootprint }   from '../api/_lib/global-music-footprint.js';
import { assembleRoyalteAI }             from '../api/_lib/royalte-ai-assembler.js';
import { assembleHealthIntelligence }     from '../api/_lib/health-intelligence.js';
import { assembleMonitoringIntelligence } from '../api/_lib/monitoring-intelligence.js';
import { computeHealthScore, generateHealthReport } from '../api/_lib/health-engine.js';
import { generateExecutiveBrief }        from '../api/_lib/executive-brief-engine.js';
import { ALL_RULES }                     from '../api/rules/index.js';

const root     = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(root, '..');

const canonical = JSON.parse(
  readFileSync(join(repoRoot, 'api/fixtures/canonical-radiohead.json'), 'utf8')
);

const cio    = assembleCio('Radiohead', { scanPayload: canonical });
const report = runIntelligenceEngine(cio, ALL_RULES);

const identityIntelligence   = assembleIdentityIntelligence(report, cio);
const publishingIntelligence = assemblePublishingIntelligence(report, cio);
const catalogEvidence        = assembleCatalogEvidence(canonical);
const catalogIntelligence    = assembleCatalogIntelligence(report, cio, catalogEvidence);
const globalMusicFootprint   = assembleGlobalMusicFootprint(report, cio, canonical);
const backendIntelligence    = assembleBackendIntelligence(canonical, publishingIntelligence);
const royalteAI              = assembleRoyalteAI(identityIntelligence, publishingIntelligence, catalogIntelligence, globalMusicFootprint);
const healthScore            = computeHealthScore(report);
const healthReport           = generateHealthReport(cio, report);
const executiveBrief         = generateExecutiveBrief(healthReport);
const healthIntelligence     = assembleHealthIntelligence(
  healthScore, identityIntelligence, publishingIntelligence,
  catalogIntelligence, globalMusicFootprint, backendIntelligence,
  null, royalteAI
);
const monitoringIntelligence = assembleMonitoringIntelligence({ scanNumber: 1, alerts: [] });

const payload = {
  ...canonical,
  identityIntelligence,
  publishingIntelligence,
  catalogIntelligence,
  globalMusicFootprint,
  backendIntelligence,
  royalteAI,
  healthIntelligence,
  healthScore,
  healthReport,
  executiveBrief,
  monitoringIntelligence,
};

// scannedAt and backendIntelligence.lastSync are set dynamically at module eval time.
// The assembler deep-freezes its output — thaw before deleting.
delete payload.scannedAt;
if (payload.backendIntelligence) {
  const bi = { ...payload.backendIntelligence };
  delete bi.lastSync;
  payload.backendIntelligence = bi;
}

const payloadJson = JSON.stringify(payload, null, 2);

const output = `// Mission Control Preview Payload
// Generated from api/fixtures/canonical-radiohead.json via all intelligence assemblers.
// scannedAt and backendIntelligence.lastSync are set dynamically so the
// "N min ago" display always shows a live value.
//
// Update this file by running:
//   node scripts/generate-preview-payload.mjs
//
// DO NOT edit this file by hand — the shape mirrors the live audit_scans.payload contract.

const _FIXTURE = ${payloadJson};

const _now = new Date(Date.now() - 4 * 60 * 1000).toISOString();

export const MC_PREVIEW_PAYLOAD = Object.freeze({
  ..._FIXTURE,
  scannedAt: _now,
  backendIntelligence: Object.freeze({
    ..._FIXTURE.backendIntelligence,
    lastSync: _now,
  }),
});
`;

const outPath = join(repoRoot, 'public/js/mc-preview-payload.js');
writeFileSync(outPath, output);
console.log(`written ${output.length} bytes → ${outPath}`);
